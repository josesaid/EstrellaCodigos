package com.codegym.lessons.lesson01;

public class ModuleOperatorExample {
    public static void main(String[] args) {
        for(int i = 1; i<=10; i++){
            if(i%2==0)
                System.out.println(i + " es PAR");
            else
                System.out.println(i + " es IMPAR");
        }
    }
}
